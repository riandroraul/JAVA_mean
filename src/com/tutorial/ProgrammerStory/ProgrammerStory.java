package ProgrammerStory;

public class ProgrammerStory {
    private String galau;
    private String happy;
    private String bored;
    private String girlfriend;

    public void setGalau(String galau) {
        this.galau = galau;
    }

    public String getGalau() {
        return this.galau;
    }

    public void setHappy(String happy) {
        this.happy = happy;
    }

    public String getHappy() {
        return this.happy;
    }

    public void setBored(String bored) {
        this.bored = bored;
    }

    public String getBored() {
        return this.bored;
    }

    public void setGirlfriend(String girlfriend) {
        this.girlfriend = girlfriend;
    }

    public String getGirlfriend() {
        return this.girlfriend;
    }

}
